import java.util.Scanner;
class OddorEven{

	public static void main (String[] args){
	Scanner sc = new Scanner (System.in);
	System.out.println ("please enter any number");
	int num;
	num=sc.nextInt();
	if (num%2==0)
	{
		System.out.print("Even number ");
	}
	else
	{
	
	System.out.print("Odd number");
	
	}
     }
} 
